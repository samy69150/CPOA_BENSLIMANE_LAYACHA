    private void AjoutFilmBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AjoutFilmBtActionPerformed
        // TODO add your handling code here:
        try {
            Film leFilm = new Film();
            FenetreAjouterFilm fenFilm = new FenetreAjouterFilm(parent,lesGenres,leFilm);
            if (fenFilm.doModal() == true) {
                lesFilms.insererInfoFilm(leFilm);
            }
            lesGenres.removeList();
        } catch (Exception e) {
            System.out.println("Exception √† l'ajout du film : " + e.getMessage());
        }
    }//GEN-LAST:event_AjoutFilmBtActionPerformed

    private void AjoutActeurBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AjoutActeurBtActionPerformed
        // TODO add your handling code here:
        int ligne = laTableR.getSelectedRow();
        int numVisa=(int)lesFilms.getValueAt(ligne,0);
        LigneActeurFilm acteur = new LigneActeurFilm();
        acteur.setNumVisa(numVisa);
        lesActeurs.viderListe();
        FenetreActeur fenActeur = new FenetreActeur(parent,lesActeurs,acteur);
        try
        {
            if (fenActeur.doModal() == true) {
                lesFilms.ajouterActeur(acteur);
            }
        } catch (Exception e) {
            System.out.println("Exception pour l'ajout d'un acteur dans un film : " + e.getMessage());
        }
    }//GEN-LAST:event_AjoutActeurBtActionPerformed

    private void AjoutRealisateurBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AjoutRealisateurBtActionPerformed
        // TODO add your handling code here:
        int ligne = laTableR.getSelectedRow();
        int numVisa=(int)lesFilms.getValueAt(ligne,0);
        LigneRealisateurFilm unRealisateur = new LigneRealisateurFilm();
        unRealisateur.setNumVisa(numVisa);
        lesRealisateurs.viderListe();
        FenetreRealisateur fenRealisateur = new FenetreRealisateur(parent,lesRealisateurs,unRealisateur);
        try
        {
            if (fenRealisateur.doModal() == true) {
                lesFilms.ajouterRealisateur(unRealisateur);
            }
        } catch (Exception e) {
            System.out.println("Exception pour l'ajout d'un acteur dans un film : " + e.getMessage());
        }
    }//GEN-LAST:event_AjoutRealisateurBtActionPerformed

